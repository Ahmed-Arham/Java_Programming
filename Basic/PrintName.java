/*
 * Program: Print Name
 * Description: Prints your name and department on separate lines.
 */

public class PrintName {
    public static void main(String[] args) {
        System.out.println("Name: Arham Ahmed");
        System.out.println("Department: Computer Science and Engineering");
        System.out.println("University: SAGE University, Bhopal");
    }
}

/*
Output:
Name: Arham Ahmed
Department: Computer Science and Engineering
University: SAGE University, Bhopal
*/
